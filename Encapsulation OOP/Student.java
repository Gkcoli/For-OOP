 public class Student{

    String name;
    int age; 
    boolean kabit;

    public void setName(String newName){
        name = newName;
    }
    public String getName(){
        return name;
    }
    public void setAge(int newAge){
        age = newAge;
    }
    public int getAge(){
        return age;
    }
    public void mayKabit(boolean merongKabit){
        kabit = merongKabit;
    }
    public boolean haveKabit(){
        return kabit;
    }
 }
